    private void cBARUActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cBARUActionPerformed
       if(cBARU.getText().equals("Baru")){
           cBARU.setText("Simpan");
           cTUTUP.setText("Batal");
           cUBAH.setEnabled(false);
           cHAPUS.setEnabled(false);
           clearForm();
           fieldEnabled(true);
       }else{
           if(!txID.getText().equals("")){
               try {
                   storeData();
                   lsDtbuku();
               } catch (SQLException ex) {
                   Logger.getLogger(formNilai.class.getName()).log(Level.SEVERE, null, ex);
               }
           }
           cBARU.setText("Baru");
           cTUTUP.setText("Tutup");
           clearForm();
           fieldEnabled(false);
           
       }
    }//GEN-LAST:event_cBARUActionPerformed

    private void cUBAHActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cUBAHActionPerformed
         if (!txID.getText().equals("")) {
        fieldEnabled(true); // ✅ Aktifkan semua field supaya bisa diketik dulu
        int confirm = JOptionPane.showConfirmDialog(this, "Yakin ingin menyimpan perubahan data ini?", "Konfirmasi Ubah", JOptionPane.YES_NO_OPTION);
        if (confirm == JOptionPane.YES_OPTION) {
            try {
                updateData();            // simpan ke database
                lsDtbuku();              // refresh tabel
                clearForm();             // kosongkan form
                fieldEnabled(false);     // kunci kembali field
                tombol(false);           // matikan tombol
                cBARU.setEnabled(true);  // aktifkan tombol Baru
                cTUTUP.setEnabled(true); // aktifkan tombol Tutup
                JOptionPane.showMessageDialog(this, "Data berhasil diubah.");
            } catch (SQLException ex) {
                Logger.getLogger(formNilai.class.getName()).log(Level.SEVERE, null, ex);
                JOptionPane.showMessageDialog(this, "Terjadi kesalahan saat mengubah data.");
            }
        }
    } else {
        JOptionPane.showMessageDialog(this, "Silakan pilih data yang ingin diubah terlebih dahulu.");
    }
    }//GEN-LAST:event_cUBAHActionPerformed

