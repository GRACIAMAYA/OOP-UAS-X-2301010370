    private void lsDtbuku() throws SQLException{
      Connection cnn = koneksi();
      dtm.getDataVector().removeAllElements();
      dtm.fireTableDataChanged();
      if( !cnn.isClosed() ){
          PreparedStatement ps = cnn.prepareStatement("SELECT * FROM nilai;");
          ResultSet rs = ps.executeQuery();
          while( rs.next() ){
              Object[] dta = new Object[4];
              dta[0] = rs.getString("nim");
              dta[1] = rs.getString("nama_mahasiswa");
               dta[2] = rs.getString("mata_kuliah");
              dta[3] = rs.getString("nilai_huruf");
             
              
            dtm.addRow(dta);
          
          }
          cnn.close();
    }
    }
    private void storeData() throws SQLException {
    Connection cnn = koneksi();
    if (!cnn.isClosed()) {
        PreparedStatement ps = cnn.prepareStatement("INSERT INTO nilai (nim, nama_mahasiswa, mata_kuliah, nilai_huruf) VALUES (?, ?, ?, ?);");
        ps.setString(1, txID.getText());
        ps.setString(2, txNAMA.getText());
        ps.setString(3, txJUDUL.getText());
        ps.setString(4, txPENULIS.getText());
        ps.executeUpdate();
        cnn.close();
    }
}

 private void updateData() throws SQLException {
    Connection cnn = koneksi();
    if (!cnn.isClosed()) {
        PreparedStatement ps = cnn.prepareStatement("UPDATE nilai SET nama_mahasiswa=?, mata_kuliah=?, nilai_huruf=? WHERE nim=?;");
        ps.setString(1, txNAMA.getText());
        ps.setString(2, txJUDUL.getText());
        ps.setString(3, txPENULIS.getText());
        ps.setString(4, txID.getText());
        ps.executeUpdate();
        cnn.close();
    }
}


    private void destoryData() throws SQLException {
    Connection cnn = koneksi();
    if (!cnn.isClosed()) {
        PreparedStatement ps = cnn.prepareStatement("DELETE FROM nilai WHERE nim=?;");
        ps.setString(1, txID.getText());
        ps.executeUpdate();
        cnn.close();
    }
}

